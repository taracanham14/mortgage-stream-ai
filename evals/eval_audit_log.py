"""
LLM-as-Judge Swarm Evaluation Suite
===================================

This script runs an automated evaluation of the non-deterministic ADK agent swarm 
end-to-end, complementing the deterministic pytest unit tests. 

Overview and Rationale:
----------------------
1. Real Pipeline Execution:
   The evaluation loads the real applicant JSON files, runs them through the 
   GDPR-compliant Privacy Shield gateway, and then triggers the underwriting swarm 
   orchestration (routing, analysis, compliance check) exactly as the FastAPI server does.
   
2. Spec-as-a-Rubric Evaluation (LLM-as-Judge):
   Because the compliance agent output is non-deterministic (free-text rationales 
   and citations generated by Gemini), we use a separate LLM-as-Judge call via `google-genai` 
   to audit the output. The judge compares the generated audit log against the intent of the 
   Gherkin acceptance scenarios from the system specification (specs/mortgagestream.md).
   
3. Rate-Limit Handling:
   To prevent rate-limit failures (HTTP 429) on the Gemini free tier (which enforces a limit 
   of 15 requests per minute), the script sleeps for 65 seconds between applicant runs 
   and applies a 65-second backoff on retries to completely clear the rate limit bucket.
   
4. Gate-based Validation:
   The judge scores three key rubric criteria from 1 to 5. If any score falls below 3, 
   the evaluation script exits with a non-zero exit code, allowing this script to act 
   as a quality gate in local build or CI/CD pipelines.

Security:
---------
- The API key is read solely from the `GOOGLE_API_KEY` environment variable.
"""

