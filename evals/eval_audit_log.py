# Execution Helper Functions
# =====================================================================

async def run_underwriting_pipeline(
    filepath: str, 
    runner: Runner, 
    session_service: InMemorySessionService
) -> dict:
    """Executes the Privacy Shield and sequential agent swarm on an applicant file."""
    with open(filepath, "r", encoding="utf-8") as f:
        raw_json_str = f.read()

    # 1. Run GDPR Privacy Shield
    redacted_json_str = scrub_application_data(raw_json_str)

    # 2. Setup fresh ADK agent session
    session_id = str(uuid.uuid4())
    await session_service.create_session(
        app_name="mortgagestream",
        user_id="underwriter",
        session_id=session_id
    )

    new_message = types.Content(
        role="user",
        parts=[types.Part(text=redacted_json_str)]
    )

    final_text = ""
    # Execute the swarm runner asynchronously exactly as FastAPI does
    async for event in runner.run_async(
        user_id="underwriter",
        session_id=session_id,
        new_message=new_message
    ):
        if event.is_final_response():
            if event.message and event.message.parts:
                parts_text = [p.text for p in event.message.parts if p.text]
                final_text = "".join(parts_text)

    # Clean markdown json codeblock wrapping from response text
    clean_text = final_text.strip()
    if clean_text.startswith("```json"):
        clean_text = clean_text[7:]
    if clean_text.endswith("```"):
        clean_text = clean_text[:-3]
    clean_text = clean_text.strip()

    try:
        return json.loads(clean_text)
    except Exception as e:
        raise ValueError(
            f"Failed to parse swarm compliance agent output as JSON.\n"
            f"Raw output: {final_text}\nError: {e}"
        )


async def run_underwriting_pipeline_with_retry(
    filepath: str, 
    runner: Runner, 
    session_service: InMemorySessionService,
    max_retries: int = 5,
    backoff_seconds: int = 90
) -> dict:
    """Wraps underwriting pipeline execution in a retry loop to handle 429 rate limits."""
    for attempt in range(max_retries):
        try:
            return await run_underwriting_pipeline(filepath, runner, session_service)
        except Exception as e:
            print(f"DEBUG: Exception caught during pipeline execution: {e}", file=sys.stderr)
            import traceback
            traceback.print_exc()
            err_str = str(e).upper()
            if "429" in err_str or "RESOURCE_EXHAUSTED" in err_str or "503" in err_str or "UNAVAILABLE" in err_str:
                print(f"Temporary API error (429/503). Sleeping for {backoff_seconds} seconds before retry (Attempt {attempt+1}/{max_retries})...")
                await asyncio.sleep(backoff_seconds)
            else:
                raise e
    raise RuntimeError(f"Exceeded maximum retries ({max_retries}) due to rate limiting.")


def evaluate_with_llm_judge(
    client: genai.Client,
    applicant_name: str,
    gherkin_scenario: str,
    audit_log: dict
) -> JudgeScorecard:
    """Invokes the LLM-as-judge to score the audit log against the specification's intent."""
    prompt = f"""
You are an expert UK mortgage underwriting auditor acting as an LLM-as-judge.
Your task is to evaluate the non-deterministic multi-agent underwriting swarm's output against the original business specifications and the deterministic calculations.

Applicant Profile: {applicant_name}
Original Spec Acceptance Criteria (Gherkin Scenario Rubric):
{gherkin_scenario}

Compliance Audit Log produced by the Swarm:
{json.dumps(audit_log, indent=2)}

Please evaluate and score the audit log (1 to 5) on the following three criteria:

1. classification_correct:
   - For the standard PAYE applicant (Oliver Twist): Must be rated "Standard" with a "Pass" affordability decision.
   - For the self-employed applicant (Ebenezer Scrooge): Must be rated "High-Risk" and routed for manual review.
   Score 5 if it perfectly matches the scenario's expected classification. Score 1 if it is incorrect.

2. dti_cited_matches_tool:
   - Does the Debt-to-Income (DTI) percentage and decision cited in the compliance agent's audit log match the correct calculation?
   - Standard PAYE applicant correct calculation: DTI ~10.38% and Pass.
   - Self-employed applicant correct calculation: DTI ~38.0% and Review.
   Score 5 if the DTI and decision figures are cited accurately and match the correct calculation. Score 1 if they are missing or incorrect.

3. rationale_explains_decision:
   - Does the rationale clearly explain the decision and explain the cited rules (e.g., FCA MCOB and Consumer Duty rules)?
   Score 5 if the rationale is detailed, explains the risk reasons (such as self-employed status, adverse credit, or vulnerability flags), and cites appropriate FCA regulations. Score 1 if it is generic or lacks detail.
"""

    response = client.models.generate_content(
        model="gemini-2.5-flash",
        contents=prompt,
        config=types.GenerateContentConfig(
            response_mime_type="application/json",
            response_schema=JudgeScorecard,
            temperature=0.0  # Keep scoring deterministic
        )
    )

    return JudgeScorecard.model_validate_json(response.text)


def evaluate_with_llm_judge_with_retry(
    client: genai.Client,
    applicant_name: str,
    gherkin_scenario: str,
    audit_log: dict,
    max_retries: int = 5,
    backoff_seconds: int = 90
) -> JudgeScorecard:
    """Wraps judge evaluation in a retry loop to handle 429 rate limits."""
    for attempt in range(max_retries):
        try:
            return evaluate_with_llm_judge(client, applicant_name, gherkin_scenario, audit_log)
        except Exception as e:
            err_str = str(e).upper()
            if "429" in err_str or "RESOURCE_EXHAUSTED" in err_str or "503" in err_str or "UNAVAILABLE" in err_str:
                print(f"Temporary API error (429/503) during judge evaluation. Sleeping for {backoff_seconds} seconds before retry (Attempt {attempt+1}/{max_retries})...")
                time.sleep(backoff_seconds)
            else:
                raise e
    raise RuntimeError(f"Exceeded maximum retries ({max_retries}) during judge evaluation due to rate limiting.")


# =====================================================================
# Main Evaluation Orchestration
# =====================================================================

async def main():
    # Define expected Gherkin scenarios to serve as rubrics
    scenarios = {
        "standard": (
            "Scenario: Standard salaried PAYE applicant with low debt is approved subject to human review\n"
            "  Given a mortgage applicant with employed PAYE status\n"
            "  And the applicant has only basic salary with no variable income\n"
            "  And the applicant has no adverse-credit flags\n"
            "  And the applicant has no vulnerability flags\n"
            "  Then the applicant's risk classification must be 'Standard'\n"
            "  And the calculated affordability decision must be 'Pass' (DTI ~10.38%)\n"
            "  And the final system recommendation must be 'Approve (Subject to human sign-off)'"
        ),
        "complex": (
            "Scenario: Self-employed applicant with variable dividends and late payments is routed for manual review\n"
            "  Given a self-employed company director applicant\n"
            "  And the applicant has variable overtime, bonus, or commission income\n"
            "  And the applicant has a 'recent_late_payment' adverse credit flag\n"
            "  Then the applicant's risk classification must be 'High-Risk'\n"
            "  And the application must be routed for deeper affordability scrutiny (DTI ~38.0%, decision 'Review')\n"
            "  And the final system recommendation must be 'Manual Review Required'"
        )
    }

    # Initialize shared pipeline components
    session_service = InMemorySessionService()
    runner = Runner(
        agent=root_agent,
        app_name="mortgagestream",
        session_service=session_service
    )

    # Initialize GenAI Client for the Judge
    if use_vertex:
        client = genai.Client()
    else:
        client = genai.Client(api_key=api_key)

    # Execute underwriting pipeline with rate limit handling
    print("Running underwriting swarm on standard_applicant.json...")
    standard_log = await run_underwriting_pipeline_with_retry("data/standard_applicant.json", runner, session_service)
    
    # Sleep disabled by developer request.
    pass
    
    print("\nRunning underwriting swarm on complex_applicant.json...")
    complex_log = await run_underwriting_pipeline_with_retry("data/complex_applicant.json", runner, session_service)

    # Sleep disabled by developer request.
    pass

    # Execute LLM Judge evaluation with rate limit handling
    print("\nRunning LLM-as-Judge evaluations...")
    standard_score = evaluate_with_llm_judge_with_retry(client, "Standard PAYE (Oliver Twist)", scenarios["standard"], standard_log)
    
    # Sleep disabled by developer request.
    pass
    
    complex_score = evaluate_with_llm_judge_with_retry(client, "Complex Self-Employed (Ebenezer Scrooge)", scenarios["complex"], complex_log)

    # Compile scorecard table
    eval_results = [
        ("Standard PAYE", "classification_correct", standard_score.classification_correct.score, standard_score.classification_correct.rationale),
        ("Standard PAYE", "dti_cited_matches_tool", standard_score.dti_cited_matches_tool.score, standard_score.dti_cited_matches_tool.rationale),
        ("Standard PAYE", "rationale_explains_decision", standard_score.rationale_explains_decision.score, standard_score.rationale_explains_decision.rationale),
        ("Complex Self-Emp", "classification_correct", complex_score.classification_correct.score, complex_score.classification_correct.rationale),
        ("Complex Self-Emp", "dti_cited_matches_tool", complex_score.dti_cited_matches_tool.score, complex_score.dti_cited_matches_tool.rationale),
        ("Complex Self-Emp", "rationale_explains_decision", complex_score.rationale_explains_decision.score, complex_score.rationale_explains_decision.rationale),
    ]

    print("\n" + "="*95)
    print(f"{'Applicant':<18} | {'Rubric Item':<28} | {'Score':<5} | {'Rationale'}")
    print("="*95)
    
    fail_flag = False
    for app_name, item, score, rationale in eval_results:
        print(f"{app_name:<18} | {item:<28} | {score:<5} | {rationale}")
        if score < 3:
            fail_flag = True
            
    print("="*95)

    if fail_flag:
        print("\n[EVALUATION FAILED] One or more rubric items scored below the acceptable threshold (score < 3).", file=sys.stderr)
        sys.exit(1)
    else:
        print("\n[EVALUATION SUCCESS] All rubric items scored 3 or above. Underwriting quality gate passed.")
        sys.exit(0)


if __name__ == "__main__":
    asyncio.run(main())
