# =====================================================================
# Judge Pydantic Schema Configuration
# =====================================================================

class RubricItem(BaseModel):
    score: int = Field(..., description="An integer score from 1 to 5.")
    rationale: str = Field(..., description="A short explanation for the given score.")

class JudgeScorecard(BaseModel):
    classification_correct: RubricItem = Field(
        ..., 
        description="Verify if the risk classification matches expected Gherkin scenario outcomes."
    )
    dti_cited_matches_tool: RubricItem = Field(
        ..., 
        description="Verify if the DTI and decision cited in the compliance agent's audit log match the correct calculation."
    )
    rationale_explains_decision: RubricItem = Field(
        ..., 
        description="Verify if the rationale is detailed, explains the risks, and cites the correct FCA regulations."
    )


